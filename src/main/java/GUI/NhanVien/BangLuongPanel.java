    private void btnLocActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLocActionPerformed
        try {
            int thang = Integer.parseInt(cboThang.getSelectedItem().toString());
            int nam = Integer.parseInt(cboNam.getSelectedItem().toString());
            String maNV = getCurrentMaNV();
            LuongBUS luongBUS = new LuongBUS();
            Luong luong = luongBUS.getByThangNam(maNV, thang, nam);

            DefaultTableModel model = new DefaultTableModel();
            model.setColumnIdentifiers(new Object[]{
                "Tháng", "Năm", "Số ngày công", "Lương cơ bản", "Tổng Thu Nhập", "Thực lãnh"
            });
            if (luong != null) {
                luong.tinhThucLanh(ngayCongChuan);
                Object[] row = new Object[]{
                    luong.getThang(),
                    luong.getNam(),
                    luong.getSoNgayCong(),
                    String.format("%,.0f", luong.getLuongCoBan()),
                    String.format("%,.0f", luong.getTongLuong()),
                    String.format("%,.0f", luong.getThucLanh())
                };
                model.addRow(row);
            }

            tblLuong.setModel(model);
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Lỗi khi tải dữ liệu lương!");
        }

    }//GEN-LAST:event_btnLocActionPerformed


