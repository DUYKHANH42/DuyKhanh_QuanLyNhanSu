    private void KhoiTaoCBo() {
        cboThang.removeAllItems();
        int thangHienTai = java.util.Calendar.getInstance().get(java.util.Calendar.MONTH) + 1;
        for (int i = 1; i <= 12; i++) {
            cboThang.addItem(i);
        }
        cboThang.setSelectedItem(thangHienTai);

        cboNam.removeAllItems();
        int namHienTai = java.util.Calendar.getInstance().get(java.util.Calendar.YEAR);
        for (int i = namHienTai - 5; i <= namHienTai + 1; i++) {
            cboNam.addItem(i);
        }
        cboNam.setSelectedItem(namHienTai);
    }

    private void KhoiTaoDuLieu() {
        try {
            String[] columns = {"Tháng", "Năm", "Số Ngày Công", "Lương Cơ Bản", "Tổng Thu Nhập", "Thực Lãnh"};
            DefaultTableModel model = new DefaultTableModel(columns, 0);

            List<Luong> listLuong = luongBUS.getByMaNV();

            for (Luong l : listLuong) {
                l.tinhThucLanh(ngayCongChuan);

                Object[] row = new Object[]{
                    l.getThang(),
                    l.getNam(),
                    l.getSoNgayCong(),
                    String.format("%,.0f", l.getLuongCoBan()),
                    String.format("%,.0f", l.getThucLanh()),
                    String.format("%,.0f", l.getTongLuong())
                };
                model.addRow(row);
            }

            tblLuong.setModel(model);

        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Lỗi khi tải dữ liệu lương!");
        }
    }

