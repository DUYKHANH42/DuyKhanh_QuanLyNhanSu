    private void tblChamCongMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblChamCongMouseClicked
        int row = tblChamCong.getSelectedRow();
        if (row >= 0) {
            // Lấy dữ liệu từ JTable theo vị trí cột như trong KhoiTaoDuLieu()
            String maCC = tblChamCong.getValueAt(row, 0).toString();
            String maNV = tblChamCong.getValueAt(row, 1).toString();
            String hoTen = tblChamCong.getValueAt(row, 2).toString();
            String phong = tblChamCong.getValueAt(row, 3).toString();
            Date ngayLam = (Date) tblChamCong.getValueAt(row, 4);
            String trangThai = tblChamCong.getValueAt(row, 5).toString();
            Object luongCBObj = tblChamCong.getValueAt(row, 6);
            Object thucLinhObj = tblChamCong.getValueAt(row, 7);
            Object tongKhauTruObj = tblChamCong.getValueAt(row, 8);
            Object tongThuNhapObj = tblChamCong.getValueAt(row, 9);
            lblMaNV.setText(maNV);
            lblTenNV.setText(hoTen);
            lblPhongBan.setText(phong);
            txtNgayLam.setDate(ngayLam);
            cboTrangThai.setSelectedItem(trangThai);

            DecimalFormat dfTien = new DecimalFormat("#,###");

            lblLuongCB.setText(luongCBObj != null ? dfTien.format(luongCBObj) + " đ" : "0 đ");
            lblLuongTL.setText(thucLinhObj != null ? dfTien.format(thucLinhObj) + " đ" : "0 đ");
            Luong luongNV = null;
            List<Luong> l = luongBUS.getAllLuong();
            for (Luong luong : l) {
                if (luong.getMaNV().equals(maNV)) {
                    luongNV = luong;
                    break;
                }
            }
            lblSoCongLam.setText(luongNV != null ? String.valueOf(luongNV.getSoNgayCong()) : "0");
            BigDecimal thuong = BigDecimal.ZERO;
            if (tongThuNhapObj != null && thucLinhObj != null) {
                try {
                    double thucLinh = Double.parseDouble(thucLinhObj.toString());
                    double tongThuNhap = Double.parseDouble(tongThuNhapObj.toString());
                    thuong = BigDecimal.valueOf(tongThuNhap - thucLinh);
                } catch (Exception e) {
                    thuong = BigDecimal.ZERO;
                }
            }
            lblThuong.setText(dfTien.format(thuong) + " đ");
        }
    }//GEN-LAST:event_tblChamCongMouseClicked
    private boolean isLocTheoNgay() {
        return chkTimKiemNgay.isSelected() && txtNgayChamCong.getDate() != null;
    }

    private boolean isLocTheoPhong() {
        PhongBan selectedPB = (PhongBan) cboPhongBan.getSelectedItem();
        return selectedPB != null && !selectedPB.getMaPhong().isEmpty();
    }
