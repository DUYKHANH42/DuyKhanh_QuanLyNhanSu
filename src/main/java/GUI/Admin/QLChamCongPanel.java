    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        try {
            String maNV = lblMaNV.getText().trim();
            Date ngayUtil = txtNgayLam.getDate();
            String trangThai = (String) cboTrangThai.getSelectedItem();

            if (maNV.isEmpty() || ngayUtil == null || trangThai == null || trangThai.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Vui lòng nhập đầy đủ thông tin!");
                return;
            }

            java.sql.Date ngayLam = new java.sql.Date(ngayUtil.getTime());

            ChamCong cc = new ChamCong();
            cc.setMaNV(maNV);
            cc.setNgayLam(ngayLam);
            cc.setTrangThai(trangThai);
            cc.getGioVao();
            cc.getGioRa();

            boolean kq = chamCongBUS.insert(cc);
            if (kq) {
                JOptionPane.showMessageDialog(this, "Thêm chấm công thành công!");
                int thang = ngayLam.toLocalDate().getMonthValue();
                int nam = ngayLam.toLocalDate().getYear();
                int tongNgayCong = chamCongBUS.countNgayCong(maNV, thang, nam);
                luongBUS.updateSoNgayCong(maNV, thang, nam, tongNgayCong);
                System.out.println(maNV + ngayUtil.toString() + trangThai + tongNgayCong);

            } else {
                JOptionPane.showMessageDialog(this, "Thêm chấm công thất bại! Kiểm tra dữ liệu hoặc nhân viên đã có ngày chấm công này.");
            }

        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Lỗi: " + e.getMessage());
        }
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        int row = tblChamCong.getSelectedRow();
        if (row < 0) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn bản ghi để xóa!");
            return;
        }

        int maCC = Integer.parseInt(tblChamCong.getValueAt(row, 0).toString());
        String maNV = tblChamCong.getValueAt(row, 1).toString();
        java.util.Date ngayLamUtil = (java.util.Date) tblChamCong.getValueAt(row, 4);
        java.sql.Date ngayLam = new java.sql.Date(ngayLamUtil.getTime());
        int confirm = JOptionPane.showConfirmDialog(this, "Bạn có chắc muốn xóa bản ghi này?", "Xác nhận", JOptionPane.YES_NO_OPTION);
        if (confirm != JOptionPane.YES_OPTION) {
            return;
        }

        boolean kq = chamCongBUS.delete(maCC);
        if (kq) {
            JOptionPane.showMessageDialog(this, "Xóa thành công!");
            int thang = ngayLam.toLocalDate().getMonthValue();
            int nam = ngayLam.toLocalDate().getYear();
            int tongNgayCong = chamCongBUS.countNgayCong(maNV, thang, nam);
            luongBUS.updateSoNgayCong(maNV, thang, nam, tongNgayCong);
            KhoiTaoDuLieu();
        } else {
            JOptionPane.showMessageDialog(this, "Xóa thất bại!");
        }
    }//GEN-LAST:event_btnXoaActionPerformed

